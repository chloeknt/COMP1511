#include <stdio.h>

int main(void) {

    //declaring variables
    int students;
    int tutors;
    int sum;
    //asking the user for the numbers
    printf ("Please enter the number of students and tutors: ");
    //scanning the user's inputs 
    scanf ("%d %d", &students, &tutors);
    //adding together
    sum = students + tutors;
    //printing the answer
    printf ("%d + %d = %d\n", students, tutors, sum);
    
    return 0;
}
